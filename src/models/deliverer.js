const mongoose = require("mongoose");

const DelivererSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  number: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
});
const Deliverer = (module.exports = mongoose.model("Deliverer", DelivererSchema));
